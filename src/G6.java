    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(jSlider3.getValue()==1968&&jSlider2.getValue()==1980){
            JOptionPane.showMessageDialog(rootPane, "Odgovor je pravilen");
            teme.poG++;
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "Odgovor ni pravilen. Združili so se 1968, razpadli pa so 1980");
            teme.noG++;
        }
        jButton3.setVisible(true);
        jButton2.setEnabled(false);
        jButton1.setEnabled(false);
        jTextField2.setText(jSlider3.getValue()+"");
        jTextField1.setText(jSlider2.getValue()+"");
        jSlider2.setEnabled(false);
        jSlider3.setEnabled(false);
        jTextField2.setEnabled(false);
        jTextField1.setEnabled(false);
    }//GEN-LAST:event_jButton2ActionPerformed

