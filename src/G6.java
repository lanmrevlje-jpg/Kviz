    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(teme.i==7){
            teme.GK=true;
            teme.cG=teme.s;
            new Odgovori().setVisible(true);
            dispose();
            
        }
        else{
        if(teme.t[teme.i]==1)
            new G1().setVisible(true);
        else if(teme.t[teme.i]==2)
            new G2().setVisible(true);
        else if(teme.t[teme.i]==3)
            new G3().setVisible(true);
        else if(teme.t[teme.i]==4)
            new G4().setVisible(true);
        else if(teme.t[teme.i]==5)
            new G5().setVisible(true);
        else if(teme.t[teme.i]==6)
            new G6().setVisible(true);
        else if(teme.t[teme.i]==7)
            new G7().setVisible(true);
        dispose();
        
        teme.i++;
        
        }
    }//GEN-LAST:event_jButton3ActionPerformed

